export function t(key: string): BiLabelType {
  return labels[key] || { zh: key, en: key };
}

/** Get bilingual text as a combined string: "中文 English" */
export function tb(key: string): string {
  const l = t(key);
  return `${l.zh} ${l.en}`;
}

